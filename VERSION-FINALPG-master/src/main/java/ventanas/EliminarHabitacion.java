  private void MostrarTabla() {

        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };

        //Array con los titulos de la tabla
        String titulos[] = {"ID", "Nº Habitacion" , "Nº Camas" , "Piso" , "AireAcondicionado", "Calefaccion"};

        //Asigno los titulos a la tabla
        modeloTabla.setColumnIdentifiers(titulos);

        List<Habitacion> listaHabitacion = hc.findHabitacionEntities(); //Relleno la lista con las facturas de la base de datos

        if (listaHabitacion != null) {

            for (Habitacion h : listaHabitacion) {
                //Array con los valores que van a ir en la tabla
                
                Object[] objeto = {h.getId(), h.getNumeroHabitacion(), h.getNumeroCamas(), h.getPiso(), h.getAireAcondicionado(), h.getCalefaccion()};

                //agrego el objeto a mi modelo de tabla 
                modeloTabla.addRow(objeto);

            }
        }

        //Le doy el modelo a la tabla
        table.setModel(modeloTabla);

    }
