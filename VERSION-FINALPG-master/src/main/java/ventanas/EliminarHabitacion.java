    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:

        List<Habitacion> listaHabitacion = hc.findHabitacionEntities(); //Relleno la lista con las facturas de la base de datos

        int idBuscada = Integer.parseInt(jTextField1.getText());

        if (listaHabitacion.contains(hc.findHabitacion(idBuscada))) {

         
            try {
                hc.destroy(idBuscada); //Borro el cliente
            } catch (IllegalOrphanException ex) {
                Logger.getLogger(EliminarHabitacion.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(EliminarHabitacion.class.getName()).log(Level.SEVERE, null, ex);
            }
       

            JOptionPane.showMessageDialog(null, "Habitacion eliminado correctamente");

        } else { //Si no salta un mensaje de error
            JOptionPane.showMessageDialog(null, "El codigo introducido no corresponde a ninguna habitacion en la base de datos");
        }
    }//GEN-LAST:event_jButton2MouseClicked

