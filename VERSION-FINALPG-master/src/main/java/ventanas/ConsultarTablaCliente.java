    private void MostrarTabla() {

        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

        };

        //Array con los titulos de la tabla
        String titulos[] = {"ID", "Nombre", "Apellidos", "DNI", "ID Tarjeta","Activo"};

        //Asigno los titulos a la tabla
        modeloTabla.setColumnIdentifiers(titulos);

        List<Cliente> listaCliente = cc.findClienteEntities(); //Relleno la lista con las facturas de la base de datos

        if (listaCliente != null) {

            for (Cliente c : listaCliente) {
                //Array con los valores que van a ir en la tabla

                Object[] objeto = null;

                if (c.getIdtarjetaD() != null) {
                    objeto = new Object[]{c.getId(), c.getNombre(), c.getApellidos(), c.getDni(), c.getIdtarjetaD().getId(), c.getActivo()};
                } else {
                    objeto = new Object[]{c.getId(), c.getNombre(), c.getApellidos(), c.getDni(), null, c.getActivo()};
                }

                //agrego el objeto a mi modelo de tabla 
                modeloTabla.addRow(objeto);

            }
        }

        //Le doy el modelo a la tabla
        table.setModel(modeloTabla);

    }

